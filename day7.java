import java.io.*;
import java.util.*;
import java.util.Scanner;

public class Solution {

    public static void main(String[] args) {
    Scanner x = new Scanner(System.in);
    int line = 0;
    
    while(x.hasNextLine()){
        line++;
        System.out.println(line+" "+x.nextLine());
        
    }

    }
    }
